def add(a,b): # 파이썬에서 함수 작성법
    return a+b
def minus(a,b): # 파이썬에서 함수 작성법
    return a-b
def multiply(a,b): # 파이썬에서 함수 작성법
    return a*b
def divide(a,b): # 파이썬에서 함수 작성법
    return a/b

sum = add(1,5)
min = minus(1,5)
mul = multiply(1,5)
div = divide(1,5)

print("sum",sum)
print("minus",min)
print("multiply",mul)
print("divide",div)